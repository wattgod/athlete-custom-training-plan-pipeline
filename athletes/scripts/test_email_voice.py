class TestGeneratePersonalEmail:
    def test_voice_rules(self, profile, parsed):
        email = generate_personal_email(profile, parsed)
        _assert_voice(email, 'personal delivery email')

    def test_subject_is_plain_and_specific(self, profile, parsed):
        email = generate_personal_email(profile, parsed)
        subject_line = email.split('\n')[0]
        assert 'Unbound 200' in subject_line
        assert 'TrainingPeaks' in subject_line
        assert '!' not in subject_line

    def test_no_exclamation_cheerleading(self, profile, parsed):
        email = generate_personal_email(profile, parsed)
        assert '!' not in email

    def test_signed_matti_gravel_god_coaching(self, profile, parsed):
        email = generate_personal_email(profile, parsed)
        assert 'Matti' in email
        assert 'Gravel God Coaching' in email

    def test_references_athlete_specifics(self, profile, parsed):
        """The email must prove the coach read the questionnaire."""
        email = generate_personal_email(profile, parsed)
        assert '245' in email  # their FTP
        assert 'finish under 14 hours' in email.lower()  # their goal
        assert 'FTP test in Week 1' in email  # estimated FTP handling

    def test_has_trainingpeaks_cta_and_reply_invite(self, profile, parsed):
        email = generate_personal_email(profile, parsed)
        assert 'home.trainingpeaks.com/attachtocoach' in email
        assert 'reply to this email' in email.lower()

    def test_no_empty_flattery_without_goal(self, profile, parsed):
        """Without a goal the ack must not fall back to filler praise."""
        profile['target_race'].pop('goal_description')
        profile['target_race'].pop('goal', None)
        email = generate_personal_email(profile, parsed)
        _assert_voice(email, 'personal delivery email (no goal)')

    def test_roadie_signature_has_no_gravel_brand_leak(self, profile, parsed):
        profile['brand'] = 'roadielabs'
        profile['discipline'] = 'road'
        profile['target_race']['name'] = 'Gran Fondo Maryland'
        email = generate_personal_email(profile, parsed)
        assert 'Roadie Labs' in email
        assert 'roadielabs.com' in email
        assert 'Gravel God' not in email
        assert 'gravelgodcycling.com' not in email


class TestEmailDeliveryCopy:
    def test_subject_no_exclamation(self):
        assert '!' not in EmailDelivery.SUBJECT
        assert EmailDelivery.SUBJECT == 'Your custom training plan is ready'

    def test_body_voice_rules(self):
        delivery = EmailDelivery()
        plain, html = delivery._build_email_body('Jesse Couch',
                                                 guide_url='https://example.com/guide')
        _assert_voice(plain, 'delivery plain text')
        _assert_voice(html, 'delivery html')
        assert '!' not in plain

    def test_body_signed_matti(self):
        delivery = EmailDelivery()
        plain, html = delivery._build_email_body('Jesse Couch')
        for body in (plain, html):
            assert 'Matti' in body
            assert 'Gravel God Coaching' in body

    def test_body_names_deliverables_and_first_action(self):
        delivery = EmailDelivery()
        plain, _ = delivery._build_email_body('Jesse Couch')
        assert 'guide' in plain.lower()
        assert 'read the phase overview' in plain.lower()
        assert 'reply to this email' in plain.lower()

    def test_roadie_sender_and_signature_have_no_gravel_brand_leak(self):
        delivery = EmailDelivery(brand='roadielabs')
        plain, html = delivery._build_email_body('Jesse Couch')
        assert delivery.from_email == 'coach@roadielabs.com'
        for body in (plain, html):
            assert 'Roadie Labs' in body
            assert 'Gravel God' not in body

    def test_delivery_cli_propagates_profile_brand(self):
        delivery = _email_delivery_for_profile({'brand': 'roadielabs'})
        plain, html = delivery._build_email_body('Jesse Couch')
        assert delivery.brand == 'roadielabs'
        assert delivery.from_email == 'coach@roadielabs.com'
        for body in (plain, html):
            assert 'Roadie Labs' in body
            assert 'Gravel God' not in body
