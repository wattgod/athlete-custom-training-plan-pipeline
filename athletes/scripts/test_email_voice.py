BANNED_PHRASES = [
    'thrilled',
    'journey',
    'crush',
    'dive in',
    "let's dive",
    'amazing',
    'excited',
    "can't wait",
    'remember:',
    'no spam',
    # retired cringe from earlier copy revisions — must not come back
    'who you are as an athlete',
    'clear and achievable',
    'you know what it takes',
]


def _contains_emoji(text):
    for ch in text:
        cp = ord(ch)
        if 0x1F000 <= cp <= 0x1FFFF:
            return True
        if 0x2600 <= cp <= 0x27BF:
            return True
        if cp in (0xFE0F, 0x200D):
            return True
    return False


def _assert_voice(text, label):
    lowered = text.lower()
    for phrase in BANNED_PHRASES:
        assert phrase not in lowered, f"banned phrase {phrase!r} in {label}"
    assert not _contains_emoji(text), f"emoji in {label}"


